<h3>About Us:</h3>

<br>
<h4>OUR VISION</h4>
<br>
Having established its footprint, KIMS vision is to become one of the
top business schools in the world. A vision where every achievement
motivates us to set the bar even higher. A vision where KIMS offerings
are continuously updated in keeping with the ever changing global
business environment. A vision where we are committed to meeting our
social responsibilities, while setting high standards for all our stake
holders. A vision and a challenge which we at KIMS have pursued
relentlessly over the years. These visions of KIMS brings a global
perspective to all its activities. Driven by this vision, KIMS has
constantly endeavored to grow and consolidate its global network. <br>
<h4>OUR AIM</h4>

Our aim is to incorporate the world's best academic practices into all
our programs, namely our management and executive program, as also our
training programs for the Top Management of the corporate world. KIMS
has consistently focused on designing practice oriented learning and a
contemporary industry-focused curriculum, driven by our strong corporate
interface. This unique blend of KIMS global perspective and strong
industry linkage contribute to bringing best practices to corporate,
through training programs as well as action-centric research and
consulting. Further, it leads to collaborative research in global,
cross-cultural areas of management, jointly with top-notch research
driven institutions world-wide. This research has led to KIMS taking a
leading role in business and economic development. As KIMS vision has
taken shape and grown, quite naturally the people associated with KIMS
have also grown in tandem. Our highly experienced faculty members have
increasingly worked on global research problems as well as research
oriented consulting, and have made significant contribution to know-how
world-wide. Our bright young students have developed their minds and
have embarked upon promising careers in the corporate world. The
executives who have been trained at KIMS have developed their latent
managerial and leadership capabilities, and several of them have made it
right to the top in their respective organizations. We are committed
towards creating a community which is vibrant and which provides a
lifelong learning experience and professional development I would like
to take this opportunity to invite you to our campus, and discover for
yourself what this great institution could mean to you. <br>


